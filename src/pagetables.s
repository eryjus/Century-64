;----------------------------------------------------------------------------------------------
; These PML4Entries each map to 512GB address space.
;----------------------------------------------------------------------------------------------

PML4Table:
			dq			LoPDPTable+0x03		; 0x0000000000000000-0x0000007fffffffff
			times(510)	dq	0				; 0x0000008000000000-0xffffff7fffffffff (incl hole)
			dq			HiPDPTable+0x03		; 0xffffff8000000000-0xffffffffffffffff

;----------------------------------------------------------------------------------------------
; These PDPEntries each map to 1GB address space.
;----------------------------------------------------------------------------------------------

LoPDPTable:
			dq			LoDirectory+0x03	; 0x0000000000000000-0x000000003fffffff
			times(511)	dq	0				; 0x0000000040000000-0x0000007fffffffff

HiPDPTable:
			times(510)	dq	0				; 0xffffff8000000000-0xffffffff7fffffff
			dq			HiDirectory+0x03	; 0xffffffff80000000-0xffffffffbfffffff
			dq			0					; 0xffffffffc0000000-0xffffffffffffffff
;----------------------------------------------------------------------------------------------
; These DirectoryEntries each map to 2MB address space.
;----------------------------------------------------------------------------------------------

LoDirectory:
			dq			LoPageTable+0x03	; 0x0000000000000000-0x00000000001fffff
			times(511)	dq	0				; 0x0000000000200000-0x000000003fffffff

HiDirectory:
			dq			HiPageTable+0x03	; 0xfffffff800000000-0xfffffff8001fffff
			dq			HiPageTable2+0x03	; 0xfffffff800200000-0xfffffff8003fffff
			times(510)	dq	0				; 0xfffffff800400000-0xfffffffbffffffff

